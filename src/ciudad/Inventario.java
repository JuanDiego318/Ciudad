    /**
     * @param args the command line arguments
     */
    private void llenarTabla(ciudades Ciudades){
        DefaultTableModel modelo =(DefaultTableModel)tabla.getModel();
        
        Vector fila = new Vector();
        
        fila.add(Ciudades.getCiudad());
        fila.add(Ciudades.getAlcalde());
        fila.add(Ciudades.getCiudadanos());
        fila.add(Ciudades.getCapital());
        fila.add(Ciudades.getEmpresaPrinc());
        
        modelo.addRow(fila);
        JOptionPane.showMessageDialog(this,"---El Articulo se ha registrado---");
    }
