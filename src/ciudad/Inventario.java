    private void BotonIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonIngresarActionPerformed
        ciudades portadora;
        portadora = new ciudades("","",0,false,"");
        
        portadora.setCiudad(campoCiudad.getText());
        campoCiudad.setText("");
        
        portadora.setAlcalde(campoAlcalde.getText());
        campoAlcalde.setText("");
        
       
        portadora.setCiudadanos((Integer)campoCiudadanos.getValue());
        campoCiudadanos.setValue(0);
        
        portadora.setCapital(campoCapital.isSelected(opcionSi.getModel()));
        campoCapital.clearSelection();
        
        portadora.setEmpresaPrinc(CampoEmpresaPrincipal.getSelectedItem().toString());
        CampoEmpresaPrincipal.setSelectedItem(null);
       
        
        llenarTabla(portadora);
    }//GEN-LAST:event_BotonIngresarActionPerformed

